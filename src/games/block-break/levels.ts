// =============================================================
// ブロックくずし: ステージ定義
// =============================================================
// 文字列アートで盤面を表す（7列×最大8行）。
//   '1' = ノーマル（1発・10点）
//   '2' = かため（2発・20点）
//   '*' = スター（1発・30点・アイテム確定ドロップ）
//   '.' = なし
// 5ステージをクリアすると、速くなって1面から周回する。
// =============================================================

export type BlockKind = '1' | '2' | '*';

export interface StageBlock {
  col: number;
  row: number;
  kind: BlockKind;
}

/** 列数（盤面の横幅はこの値を前提にレイアウトする） */
export const COLS = 7;

export const STAGES: readonly (readonly string[])[] = [
  // 1: ならんだブロック（やさしい入門）
  [
    '.......',
    '.11111.',
    '.1*1*1.',
    '.11111.',
  ],
  // 2: しましま
  [
    '1111111',
    '.......',
    '2.2.2.2',
    '.......',
    '11*1*11',
    '.......',
    '.11111.',
  ],
  // 3: ダイヤモンド
  [
    '...1...',
    '..111..',
    '.11211.',
    '1122211',
    '.11211.',
    '..1*1..',
    '...1...',
  ],
  // 4: とりで
  [
    '2.....2',
    '21...12',
    '211*112',
    '2111112',
    '.11111.',
    '..***..',
  ],
  // 5: さいごのかべ
  [
    '2222222',
    '2111112',
    '21***12',
    '2111112',
    '2222222',
    '.......',
    '.1.1.1.',
  ],
];

/** 文字列アートをブロック定義の配列にする */
export function parseStage(rows: readonly string[]): StageBlock[] {
  const out: StageBlock[] = [];
  rows.forEach((line, row) => {
    const n = Math.min(COLS, line.length);
    for (let col = 0; col < n; col++) {
      const ch = line[col];
      if (ch === '1' || ch === '2' || ch === '*') out.push({ col, row, kind: ch });
    }
  });
  return out;
}
